// ─── Branch name format ────────────────────────────────────────────────

describe("Branch name formatting from codver.ts", () => {
  // Tests for the branch name logic in main()
  // If --new-branch is provided:
  //   newBranch = `codver/${sanitizeBranchName(newBranch.replace(/^codver\/?/, ""))}`
  // If --new-branch is not provided:
  //   Generated by AI, already prefixed with codver/

  test("provided branch name gets codver/ prefix after stripping existing prefix", () => {
    // Simulate the logic from codver.ts
    function sanitizeBranchName(name: string): string {
      return name
        .toLowerCase()
        .replace(/[^a-z0-9\-]/g, "-")
        .replace(/-+/g, "-")
        .replace(/^-|-$/g, "")
        .slice(0, 50);
    }

    // Case 1: Simple branch name
    let newBranch = "my-feature";
    newBranch = `codver/${sanitizeBranchName(newBranch.replace(/^codver\/?/, ""))}`;
    expect(newBranch).toBe("codver/my-feature");

    // Case 2: Already has codver/ prefix
    newBranch = "codver/my-feature";
    newBranch = `codver/${sanitizeBranchName(newBranch.replace(/^codver\/?/, ""))}`;
    expect(newBranch).toBe("codver/my-feature");

    // Case 3: Has codver prefix without slash
    newBranch = "codver-add-auth";
    newBranch = `codver/${sanitizeBranchName(newBranch.replace(/^codver\/?/, ""))}`;
    expect(newBranch).toBe("codver/add-auth");

    // Case 4: Special characters
    newBranch = "Add Feature & Fix Bug!";
    newBranch = `codver/${sanitizeBranchName(newBranch.replace(/^codver\/?/, ""))}`;
    expect(newBranch).toBe("codver/add-feature-fix-bug");
  });
});