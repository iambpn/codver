// ─── ValidationError usage in main ─────────────────────────────────────

describe("ValidationError in main catch block", () => {
  test("ValidationError is correctly caught and distinguished from other errors", () => {
    const validationErr = new ValidationError("Missing --repo");
    expect(validationErr instanceof ValidationError).toBe(true);
    expect(validationErr instanceof Error).toBe(true);
    expect(validationErr.message).toBe("Missing --repo");
    expect(validationErr.name).toBe("ValidationError");

    const regularErr = new Error("Something went wrong");
    expect(regularErr instanceof ValidationError).toBe(false);
  });
});

// ─── GITIGNORE_ENTRIES consistency ─────────────────────────────────────

describe("GITIGNORE_ENTRIES consistency with main pipeline", () => {
  test("entries match what the pipeline writes to .gitignore", () => {
    // The pipeline adds these entries to .gitignore
    expect(GITIGNORE_ENTRIES).toContain("docker-compose.dev.yml");
    expect(GITIGNORE_ENTRIES).toContain("bunfig.toml");
    expect(GITIGNORE_ENTRIES).toContain(".env");
    expect(GITIGNORE_ENTRIES).toContain(".codver-plan");
  });

  test("the dev files excluded from git add are covered by GIT_STAGING_EXCLUSIONS", () => {
    // codver.ts uses GIT_STAGING_EXCLUSIONS to build the `:!pattern` args for git add.
    // All CODVER_REPO_PATTERNS must be in the exclusions list.
    for (const pattern of CODVER_REPO_PATTERNS) {
      expect(GIT_STAGING_EXCLUSIONS).toContain(pattern);
    }

    // Standard .gitignore entries are also explicitly excluded
    expect(GIT_STAGING_EXCLUSIONS).toContain("node_modules");
    expect(GIT_STAGING_EXCLUSIONS).toContain(".DS_Store");
  });
});

